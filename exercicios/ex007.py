# Mostrar Média Aritmética

# entrada
print("~ Média Aritmética", "-"*20)
nota1 = float(input("Primeira nota do aluno: "))
nota2 = float(input("Segunda nota do aluno: "))

# processamento/ calculo de média
media = (nota1 + nota2) / 2

# saída
print("~ Resultado", "-"*26)
print(f"Média do aluno: {media:.1f}")